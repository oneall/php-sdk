// HTTP Handler and Configuration
include '../assets/config.php';

// Users \ Delete a user
// http://docs.oneall.com/api/resources/users/delete-user/

// The user to delete (Deleting a user will also remove the linked identities)
$user_token = '96227cb2-bc0a-497b-bcdf-2773a9302f4a';

// Make Request
$oneall_curly->delete (SITE_DOMAIN . "/users/" . $user_token . ".json?confirm_deletion=true");
$result = $oneall_curly->get_result ();

// Success
if ($result->http_code == 200)
{
	echo "<h1>Success " . $result->http_code . "</h1>";
	echo "<pre>" . oneall_pretty_json::format_string ($result->body) . "</pre>";
}
// Not found
elseif ($result->http_code == 404)
{
	echo "<h1>User not found " . $result->http_code . "</h1>";
	echo "<pre>" . oneall_pretty_json::format_string ($result->body) . "</pre>";
}
// Error
else
{
	echo "<h1>Error " . $result->http_code . "</h1>";
	echo "<pre>" . oneall_pretty_json::format_string ($result->body) . "</pre>";
}

?>